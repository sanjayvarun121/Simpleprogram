package day4;

public class Multidime 
{

	public static void main(String[] args) 
	{
		int a[][]= {{6,7,6,7},{7,8,9,7},{5,6,7,7}};
		
		for(int i=0;i<3;i++) 
		{
			for(int j=0;j<4;j++) 
			{
				System.out.print(a[i][j]+" ");
				
			}
			System.out.println(" ");
}
		

	}

}
